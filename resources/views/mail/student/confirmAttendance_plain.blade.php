Ja tu saņēmi šo e-pastu, tad Tu esi pieteicies Datorikas fakultātes pirmsaristotelim "Sējiens 2022".

Atgādinām, ka pasākums ir bez dalības maksas, par ko izsakām milzīgu paldies mūsu draugiem – Accenture un Tietovery!

Gaidīsim Tevi Sējiena pirmajā daļā piektdien, 2. septembrī Datorikas fakultātes telpās, Raiņa bulvārī 19. Pirmās daļas laikā izpētīsim universitātes telpas un iepazīsimies ar saviem jaunajiem kursabiedriem.

Turpinot Sējienu, kopīgi dosimies uz kādu Datoriķiem tuvu vietu, tepat Latvijā, kādas stundas braucienā no Rīgas (vai varbūt arī ne?), kur vakarā kopīgi pavadīsim laiku, atpūtīsimies, iepazīsim savus kursa biedrus, un vienkārši pavadīsim labi laiku, lai sagatavotos nākamajai dienai.
Sējiena otrajā dienā tevi sagaida dažādas sportiskas aktivitātes, kurās pārbaudīs tavas spējas dažādos aspektos, kā arī būs vieslekcijas no mūsu fantastiskajiem sponsoriem, un baumo, ka vakarā būs jau tavs pirmais baļļuks studentijā!

Un sējiena pēdējajā dienā, trešajā dienā, kopīgi dosimies uz Rīgu, lai dotos uz Aristoteļa svētkiem Doma Laukumā.

Lūdzu, aizpildi apstiprinājuma anketu dalībai pasākumā! It īpaši, ja ir kādas alerģijas vai uztura ierobežojumi. (Būs pieejama līdz 1. septembrim.) {{ route('student.confirmation', $student->hash) }}
Kad esi to izdarījis – apsveicam, tagad Tu esi dalībnieku sarakstā, tāpēc vari sākt gatavoties pasākumam. Iesakam arī uzmest aci šīs dienas laikapstākļu prognozei, jo kā tautā saka - nav nepiemēroti laikapstākļi, ir nepiemērots apģērbs!

Un pat tad, ja kādu mistisku iemeslu dēļ pēkšņi netiec uz Sējienu, noteikti atzīmē sev kalendārā, ka 4. septembrī plkst. 17.40 isi tiekamies Doma laukumā, uz Aristoteļa oficiālo daļu, un tad kopā gājienā uz Raiņa bulvāri 19. Tu jau nevarētu ierasties!

