Atgādinām, ka pasākums ir bez dalības maksas, par ko izsakām milzīgu paldies mūsu draugiem no Accenture!

Gaidīsim Tevi Sējiena pirmajā daļā piektdien, 1. septembrī Datorikas fakultātes telpās, Raiņa bulvārī 19. Pirmās daļas laikā iepazīsiemies ar saviem jaunajiem kursabiedriem un izpētīsim universitātes telpas.

Kad būsim iepazinušies un izpētījuši fakultātes gaiteņus, kopīgi dosimies nelielā izbraucienā uz mūsu nākamo pieturu ārpus Rīgas, kur pirmās dienas vakarā atpūtīsimies, turpināsim iepazīt viens otru, un uzkrāsim spēkus nākamajai dienai.

Sējiena otrajā dienā Tevi sagaidīs dažādas sportiskas aktivitātes, kuras pārbaudīs tavas spējas dažādos aspektos. Un baumo, ka vakarā būs ballīte!

Un sējiena pēdējā dienā, trešajā dienā, kopīgi dosimies atpakaļ uz Rīgu, lai kopīgi dotos uz Aristoteļa svētkiem Doma Laukumā.

Lūdzu, aizpildi apstiprinājuma anketu dalībai pasākumā! It īpaši, ja ir kādas alerģijas vai uztura ierobežojumi. (Būs pieejama līdz 25.augustam.) {{ route('student.confirmation', $student->hash) }}
Kad esi aizpildījis apstiprinājuma anketu – apsveicam, Tu esi dalībnieku sarakstā. Tāpēc vari sākt gatavoties Sējienam.

Un pat tad, ja biji aizpildījis apstiprinājuma anketu un kādu mistisku iemeslu dēļ pēkšņi netiec uz Sējienu, paziņo to mums, atzīmē sev kalendārā, ka 3. septembrī tiekamies Doma laukumā, uz Aristoteļa oficiālo daļu!

Svarīgi!
Sējiena pirmā daļa norisināsies 1. septembrī, Raiņa bulvārī 19. Reģistrācija no plkst. 9.00, sākums ir plkst.10.00.
Sējiena otrā daļa norisināsies ārpus Rīgas, kādas stundas braucienā no Rīgas, izbraukšana plkst. 15:40 no Aspazijas bulvāra 5.
Atpakaļ Rīgā būsim 3.septembrī, plkst. 14:50, un plkst. 17:20 tiksimies atkal pie Raiņa bulvāra 19, lai kopīgi dotos uz Aristoteļa svētkiem Doma laukumā, kuri sākas plkst. 18:00.

